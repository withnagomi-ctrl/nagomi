// ====================
// HELPERS
// ====================

function shuffle(array) {
  const copy = [...array]

  for (let i = copy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))

    ;[copy[i], copy[j]] = [copy[j], copy[i]]
  }

  return copy
}

function pickRandom(array, amount) {
  return shuffle(array).slice(0, amount)
}

// ====================
// MAIN GENERATOR
// ====================

export function generateSeedPosts(anime) {
  const prompts = []

  // always include general discussion starters
  prompts.push(...pickRandom(universalPrompts, 3))

  // status-based prompts
  if (
    anime.status === 'Currently Airing' ||
    anime.status === 'Not yet aired'
  ) {
    prompts.push(...pickRandom(releasingPrompts, 2))
  } else {
    prompts.push(...pickRandom(finishedPrompts, 2))
  }

  // genre-based prompts ONLY (primary driver)
  if (anime.genres?.length) {
    anime.genres.forEach((genre) => {
      const pool = promptPools[genre]

      if (pool?.length) {
        prompts.push(...pickRandom(pool, 3))
      }
    })
  }

  // optional hot takes (low frequency)
  if (Math.random() > 0.7) {
    prompts.push(...pickRandom(hotTakePrompts, 1))
  }

  // keep it structured + consistent
  const finalPrompts = shuffle(prompts).slice(0, 8)

  return finalPrompts.map((prompt) => ({
    content: prompt.replaceAll('{anime}', anime.title),

    mood: null,
    spoiler_level: 'none',
    post_type: 'discussion', // 👈 important change

    is_seeded: true,
  }))
}